#!/usr/bin/env bash
#
# Regenerates the bundled third-party libraries under the Joomlatools\ namespace
# prefix, plus the classmap autoloader that loads them.
#
# Why: Joomla (and other extensions) ship their own copies of some of these
# libraries in their ORIGINAL global namespaces (e.g. Joomla 6 ships its own
# Cron\CronExpression with a different constructor signature). Loading our copies
# under those same namespaces collides in the shared runtime. Relocating ours
# under Joomlatools\ makes a clash impossible.
#
# The list of libraries is NOT maintained here. It is whatever is declared in
# composer.json "require": Strauss scopes every required package, and the
# autoloader is generated by scanning the result. To add or remove a library,
# edit composer.json "require" and re-run this script — nothing else.
#
# Output: a single self-contained vendor/ directory (the scoped package folders +
# composer/ + autoload.php) committed to the repository.
#
# Usage: composer scope-vendor   (or: bash build/scope-vendor.sh)
#
set -euo pipefail
cd "$(dirname "$0")/.."
LIB="$PWD"

WORK="$(mktemp -d)"
MIN="$(mktemp -d)"
trap 'rm -rf "$WORK" "$MIN"' EXIT

# 1. Install the required libraries + Strauss in an isolated workspace, then
#    relocate every required package under the Joomlatools\ prefix. composer.json
#    is the single source of truth for both the dependency list and the Strauss
#    configuration (no "packages" key -> Strauss scopes all of "require").
#
#    The manifest is copied WITHOUT its "scripts": this script is itself invoked
#    from composer's post-install/post-update hooks, and the workspace install
#    below must not re-trigger them (infinite recursion, and build/ does not exist
#    in $WORK). php is already required to run Strauss, so we reuse it here.
php -r '$d = json_decode(file_get_contents("composer.json"), true); unset($d["scripts"]); file_put_contents($argv[1], json_encode($d, JSON_PRETTY_PRINT | JSON_UNESCAPED_SLASHES));' "$WORK/composer.json"
# composer-patches resolves the patch paths in "extra.patches" relative to the
# manifest, so the patch files must travel into the workspace alongside it.
mkdir -p "$WORK/build/patches"
cp build/patches/*.patch "$WORK/build/patches/"
( cd "$WORK" && composer install --no-interaction )
( cd "$WORK" && php vendor/bin/strauss )

# 2. Publish the prefixed sources into a clean vendor/, dropping Strauss's own
#    autoloader artifacts (we generate a composer one below).
rm -rf "$LIB/vendor"
mkdir -p "$LIB/vendor"
cp -R "$WORK"/scoped/* "$LIB/vendor/"
rm -f "$LIB"/vendor/autoload.php "$LIB"/vendor/autoload-classmap.php

# 3. Build an authoritative classmap autoloader by scanning the whole scoped
#    vendor/ (everything except composer/). Done in a Strauss-free workspace so
#    no build-time dependency leaks into the runtime autoloader.
mkdir -p "$MIN/vendor"
cp -R "$LIB"/vendor/* "$MIN/vendor/"
cat > "$MIN/composer.json" <<'JSON'
{
    "autoload": {
        "classmap": ["vendor"],
        "exclude-from-classmap": ["/composer/"]
    },
    "config": { "classmap-authoritative": true }
}
JSON
( cd "$MIN" && composer dump-autoload --classmap-authoritative --optimize --no-interaction )
cp -R "$MIN"/vendor/composer "$LIB/vendor/composer"
cp "$MIN"/vendor/autoload.php "$LIB/vendor/autoload.php"

echo "Scoped vendor regenerated: $(grep -c '=>' "$LIB/vendor/composer/autoload_classmap.php") classes."
